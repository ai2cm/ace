class MLP(nn.Module):
    def __init__(self, in_features, hidden_features=None, out_features=None, act_layer=nn.GELU, drop=0.):
        super(MLP, self).__init__()
        out_features = out_features or in_features
        hidden_features = hidden_features or in_features
        
        # first set of hp
        self.w1 = nn.Parameter(torch.ones(hidden_features, in_features, 1, 1))
        self.b1 = nn.Parameter(torch.zeros(hidden_features))
        
        # second set of hp
        self.w2 = nn.Parameter(torch.ones(out_features, hidden_features, 1, 1))
        self.b2 = nn.Parameter(torch.zeros(out_features))
        
        self.act = act_layer()
        self.drop = nn.Dropout(drop) if drop > 0. else nn.Identity()
        
        # init weights
        self._init_weights()

    def _init_weights(self):
        trunc_normal_(self.w1, std=.02)
        nn.init.constant_(self.b1, 0.)
        trunc_normal_(self.w2, std=.02)
        nn.init.constant_(self.b2, 0.)

        
    def forward(self, x):
        
        torch.cuda.nvtx.range_push("MLP")
        
        x = F.conv2d(x, self.w1, bias=self.b1)        
        x = self.act(x)
        x = self.drop(x)
        x = F.conv2d(x, self.w2, bias=self.b2)        
        x = self.drop(x)
        
        torch.cuda.nvtx.range_pop()
        
        return x 

    
